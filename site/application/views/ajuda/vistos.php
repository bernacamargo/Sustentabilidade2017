                <h2>Tipos de vistos</h2>
                <p>
                    Antes de iniciar o processo de retirada do visto, é importante que você saiba que a sua intenção no país é que definirá o tipo de visto de viagem que deverá solicitar.
                </p>

                <p>
                    No Brasil existem os seguintes tipos de visto:
                    <br><br>
                    <b>Visto de Turismo – </b>voltado para viagens curtas com objetivo de turismo, visita à casa de parentes e amigos, participação de conferências ou seminários, competições artísticas ou de esporte amador;
                    <br><br>
                    <b>Visto de Trânsito – </b>voltado a estrangeiros que tenham que entrar no Brasil para ingressar no país de destino.
                    <br>
                    *Não é necessário este visto em caso de escalas e conexões no Brasil, desde que o viajante permaneça no aeroporto brasileiro;
                    <br><br>
                    <b>Visto de Negócios – </b>para quem realizará alguma atividade profissional no Brasil;
                    <br><br>
                    <b>Visto Diplomático – </b>para missões diplomáticas;
                    <br><br>
                    <b>Visto Oficial – </b>para missões oficiais;
                    <br><br>
                    <b>Visto de Cortesia – </b>voltado para estrangeiros convidados por cidadãos brasileiros;
                    <br><br>
                    <b>Visto Permanente – </b>voltado para quem deseja morar no Brasil;
                    <br><br>
                    <b>Visto Temporário – </b>para quem ficará por um curto período de tempo no país seja por motivo de intercâmbio, estágio, pesquisa científica, tratamento de saúde, atividades artísticas ou esportivas, negócios, trabalho e missões religiosas. Para cada tipo de atividade há um visto temporário específico.
                    <br><br>
                    <b>Visto de Residência Temporária – </b>voltado para quem deseja viver por tempo determinado no Brasil.           
                    <br><br>
                    Para mais informações acesse o seguinte <a href="http://www.portalconsular.itamaraty.gov.br/vistos-para-viajar-ao-brasil" target="_blank">site</a>.
                </p>
<br>
                <h2>
                    Como obter o visto brasileiro
                </h2>
<br>
                <h3>1. Preencha o formulário de pedido de visto</h3>

                <p>
                    Primeiramente é necessário acessar o site do Ministério das Relações Exteriores e preencher o formulário de pedido do visto.
<br><br>
                    Nesta <a target="_blank" href="https://formulario-mre.serpro.gov.br/sci/pages/web/pacomPasesWebInicial.jsf">página</a> você poderá selecionar a língua em que deseja ler as instruções e preencher o formulário. Entre as opções temos: inglês, francês, espanhol e alemão.
<br><br>
                    Depois basta selecionar a opção “Visto”, você será encaminhado a uma nova página com informações sobre o preenchimento do formulário e após ler deverá clicar em “Iniciar Novo Requerimento” para ter acesso ao formulário.
<br><br>
                    Ele deve ser preenchido da forma mais completa possível para agilizar o processo de retirada do visto.
<br><br>
                    Após o preenchimento você deve imprimir e assinar o Recibo de Entrega do Requerimento (RER), o qual conterá o número do protocolo do pedido.

                </p>


                <h3>2. Agende sua visita no consulado</h3>

                <p>
                    É importante que você entre em contato com o consulado brasileiro de seu país de origem e agende uma visita.
<br><br>
                    Confira no site <a href="http://consulados.com.br/brasil/" target="_blank">Consulados</a> a lista das sedes dos consulados brasileiros no exterior.
                </p>

                <h3>3. No consulado</h3>

                <p>
                    Ao chegar ao consulado você deve portar os seguintes documentos:
                    <ul style="margin-left: 50px;">
                        <li>
                            Formulário de Pedido de Visto preenchido;        
                        </li>
                        <li>
                            Documento de viagem válido – Cédula de identidade para os países do Mercosul e passaporte para os demais;        
                        </li>
                        <li>
                            Comprovante de pagamento da taxa do visto de viagem;
                        </li>
                        <li>
                            Certificado Internacional de Imunização – vacinação contra febre amarela em caso de viajantes que venham de regiões provenientes da doença;
                        </li>
                        <li>
                            Documentos pessoais e comprovantes – os documentos pedidos variam de acordo com o solicitante, servindo para comprovar sua cidadania e vínculos no país de origem, assim como sua renda para provar se tem condições de custear a viagem.
                        </li>
                    </ul>
                    Você entregará os documentos ao agente consular que irá conferi-los e analisá-los a fim de aceitar ou não sua solicitação.
                    <br><br>
                    Caso ela seja aceita o visto será entregue em no prazo de no máximo 90 dias.                    
                </p>
